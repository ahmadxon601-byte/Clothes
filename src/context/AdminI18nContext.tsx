const AdminI18nContext = createContext<I18nCtx | null>(null);

export function AdminI18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<AdminLocale>(() => {
    if (typeof window === 'undefined') return 'uz';
    const stored = localStorage.getItem(STORAGE_KEY) as AdminLocale | null;
    return stored === 'uz' || stored === 'ru' || stored === 'en' ? stored : 'uz';
  });

  const setLocale = (next: AdminLocale) => {
    setLocaleState(next);
    localStorage.setItem(STORAGE_KEY, next);
  };

  const t = (key: string, vars?: Record<string, string | number>) => {
    const template = DICTIONARY[locale][key] ?? DICTIONARY.uz[key] ?? key;
    if (!vars) return template;
    return Object.entries(vars).reduce(
      (acc, [name, value]) => acc.replace(new RegExp(`\\{${name}\\}`, 'g'), String(value)),
      template,
    );
  };

  const value = useMemo(() => ({ locale, setLocale, t }), [locale]);

  return <AdminI18nContext.Provider value={value}>{children}</AdminI18nContext.Provider>;
}

export function useAdminI18n() {
  const ctx = useContext(AdminI18nContext);
  if (!ctx) throw new Error('useAdminI18n must be used within AdminI18nProvider');
  return ctx;
}
