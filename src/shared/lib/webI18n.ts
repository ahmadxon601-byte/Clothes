export function useWebI18n() {
    const language = (useSettingsStore((s) => s.settings.language) || 'uz') as WebLanguage;
    const w = WEB_TRANSLATIONS[language] || WEB_TRANSLATIONS.uz;
    const tc = (category: string) => CATEGORY_LABELS[language]?.[category] ?? category;

    return { w, language, tc };
}

